        public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel) tblWarehouseDetails.getModel();
        
        model.setRowCount(0);

        Object[] row = new Object[3];
        for(Warehouse ware:business.getWarehouseDirectory().getWarehouseList()){
             row[0] = ware;
             row[1] = ware.getWarehouseAddress();
             row[2] = ware.getWarehouseContact();
             model.addRow(row);
        }
    }
  
